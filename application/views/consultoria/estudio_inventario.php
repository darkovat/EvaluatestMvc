							<li>Identifica objetivamente las necesidades de los puestos y de los participantes por medio de
								los Reportes de resultados por competencias laborales, y permite definir a partir de ellas los 
								planes de desarrollo y capacitación, asegurando una inversión alineada a las necesidades de la 
								organización.</li>
						</ul>

