					<h2 class='titleB color_orange'>Inventario de Talento</h2>
					<p>
						El  Estudio de clima y satisfacción laboral es una herramienta estratégica para conocer la opinión 
						de los colaboradores. Los resultados que arroja permiten tomar decisiones efectivas para gestionar 
						estratégicamente los programas y planes de acción alineados tanto con los objetivos del negocio, como 
						con las necesidades del personal.
					</p>
					<p>
						Con el diagnóstico obtenido, se pueden establecer prioridades que incrementen el nivel de satisfacción
						en la organización, creando un ambiente propicio para la productividad, la motivación, el compromiso y 
						la efectividad de los equipos de trabajo.
					</p>
					<p>
						La aplicación del estudio es muy fácil y permite obtener resultados que pueden ser presentados
						oportunamente a la Dirección de la empresa y establecer acciones de mejora.
					</p>
				</div>

				<div id='alcance' class='seccion'>
					<strong>Esta solución permitirá:</strong>
					<ul class='list'>
						<li>Conocer el nivel de satisfacción general de los colaboradores con respecto a la empresa.</li>
						<li>Identificar las fortalezas y áreas de oportunidad más notables de la organización.</li>
						<li>Mejorar los sistemas de comunicación interna y externa.</li>
						<li>Mejorar los procesos productivos.</li>
						<li>Realizar una adecuada planeación estratégica.</li>
						<li>Llevar a cabo cambios en el entorno organizacional Interno.</li>
						<li>Implementar planes de acción y mejora a partir de los resultados.</li>
						<li>Asegurar que los programas y planes de trabajo de la organización están sincronizados con las
							necesidades de los colaboradores.</li>
						<li>Tomar decisiones y acciones orientadas a incrementar el desempeño individual y de la
							organización para el cumplimiento de sus metas de negocio.</li>
					</ul>
				</div>

				<div id='incluye' class='seccion'>
					<p>El estudio incluye el servicio de consultoría para la implementación, análisis e interpretación de resultados y el uso de la plataforma Evaluatest®
						para la aplicación de las evaluaciones correspondientes.
					</p>
	
					<p>
						<strong>Entregables:</strong>
						<ul class='list'>
							<li>Asesoría durante las distintas etapas del proceso.</li>
							<li>Perfil ideal del asesor y gerente (académico, demográfico y por competencias laborales).</li>
							<li>Perfil no deseado (académico, demográfico y por competencias laborales).</li>
							<li>Identificación de los tipos de colaboradores (high potential, top y low performers).</li>
							<li>Reportes de resultados individuales, donde se muestra el nivel de compatibilidad del asesor con su puesto.</li>
							<li>Reporte de resultados grupales. Se entrega un Six box grid en el cual se muestra con nombre y apellido la clasificación de sus colaboradores, según el nivel de compatibilidad con el puesto contra desempeño.</li>
							<li>Detección de necesidades de capacitación (DNC) por área, puesto y persona.</li>
							<li>Principales fortalezas y áreas de oportunidad de acuerdo con los niveles de puesto.</li>
							<li>Base de datos con los resultados consolidados.</li>
							<li>Recomendaciones a seguir.</li>
							<li>Análisis y presentación ejecutiva de resultados.</li>
						</ul>
					</p>
					<p>
						<strong>Modelo de servicio y atención a clientes:</strong>
						<ul class='list'>
							<li>Soporte vía telefónica en México.</li>
							<li>Atención por canales electrónicos (vía chat, skype y correo electrónico) en Latinoamérica.</li>
							<li>Programa de seguimiento y aseguramiento del servicio 1,4,9.</li>
						</ul>
					</p>
