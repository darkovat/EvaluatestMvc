							<li>Proporciona información para la identificación de los colaboradores low y top performers.</li>
							<li>Potencia la toma de decisiones acertadas para el desarrollo y retención del talento.</li>
							<li>Posibilita la generación de parrillas de sucesión o reemplazo.</li>
						</ul>

					</p>
