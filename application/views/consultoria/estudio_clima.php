				<div id='descripcion' class='seccion'>
					<h2 class='titleB color_orange'>Clima y Satisfacción</h2>
					<p>
						Es la solución que se apoya de la plataforma tecnológica Evalulatest obtener el diagnóstico requerido. De esta manera, la empresa realiza una implementación, análisis e interpretación de resultados adecuado.
					</p>
					<p>
						Este estudio permite  identificar el nivel de compatibilidad de cada uno de los colaboradores con la 
						posición a la que pertenecen, detectando sus fortalezas y áreas de oportunidad relacionadas con las 
						competencias laborales requeridas por el perfil del puesto. Así, al identificar al personal de alto potencial 
						y desempeño, se favorece la toma de decisiones efectiva, incrementando la productividad del equipo por 
						medio de planes de desarrollo y capacitación.
					</p>
				</div>

				<div id='alcance' class='seccion'>
					<strong>Esta solución permitirá:</strong>
					<ul class='list'>
						<li>Conocer el perfil de competencias laborales de los colaboradores y el grado de compatibilidad con el puesto.</li>
						<li>Determinar las necesidades de capacitación (DNC) del equipo y saber a quién capacitar y en qué.</li>
						<li>Contar con información precisa para definir los planes de desarrollo.</li>
						<li>Actualizar la información de los colaboradores: nivel académico, datos demográficos y trayectoria laboral.</li>
						<li>Conocer el desarrollo de habilidades  soft y  hard de los colaboradores en un periodo de tiempo y determinar el impacto real de los programas de capacitación y desarrollo.</li>
						<li>Identificar y establecer los perfiles ideales por puesto, que servirán como parámetro para la atracción y selección de nuevos colaboradores.</li>
						<li>Hallar y reformular los perfiles no deseados, los que comúnmente rotan o no dan los resultados que se esperan.</li>
						<li>Identificar a los colaboradores high potential con el objetivo de trabajar conjuntamente para apuntalar el crecimiento de la organización.</li>
					</ul>
				</div>

